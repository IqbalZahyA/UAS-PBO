    private void btnregisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregisActionPerformed
        // TODO add your handling code here:
    String memberId = txid.getText().trim();
    String memberName = txtname1.getText().trim(); // nama asli
    String username = txtname.getText().trim();    // username login
    String password = txtPassword.getText().trim();

    if (username.isEmpty() || password.isEmpty() || memberName.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Semua kolom wajib diisi!");
    } else {
        try {
            Connection c = Koneksi.getKoneksi();

            // 1. Masukkan ke tabel user
            String insertUser = "INSERT INTO user (username, password, role) VALUES (?, ?, 'member')";
            PreparedStatement pstUser = c.prepareStatement(insertUser, Statement.RETURN_GENERATED_KEYS);
            pstUser.setString(1, username);
            pstUser.setString(2, password);
            pstUser.executeUpdate();

            // 2. Ambil ID user terakhir
            ResultSet rs = pstUser.getGeneratedKeys();
            int userId = -1;
            if (rs.next()) {
                userId = rs.getInt(1);
            }

            // 3. Masukkan ke tabel members
            String insertMember = "INSERT INTO members (member_id, member_name, user_id) VALUES (?, ?, ?)";
            PreparedStatement pstMember = c.prepareStatement(insertMember);
            pstMember.setString(1, memberId);
            pstMember.setString(2, memberName);
            pstMember.setInt(3, userId);
            pstMember.executeUpdate();

            JOptionPane.showMessageDialog(null, "Akun berhasil dibuat!");
            this.dispose();
            new Login().setVisible(true);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
            e.printStackTrace();
        }
    }
    }//GEN-LAST:event_btnregisActionPerformed

