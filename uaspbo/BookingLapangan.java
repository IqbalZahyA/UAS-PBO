    private void autonumberBooking() {
    try {
        Connection c = Koneksi.getKoneksi();
        Statement s = c.createStatement();
        ResultSet r = s.executeQuery("SELECT * FROM booking ORDER BY id_booking DESC");

        if (r.next()) {
            String lastId = r.getString("id_booking").substring(1); // B001 → 001
            int nextId = Integer.parseInt(lastId) + 1;
            String newId = String.format("B%03d", nextId); // → B002
            txtidbooking.setText(newId);
        } else {
            txtidbooking.setText("B001");
        }

        r.close();
        s.close();
    } catch (Exception e) {
        e.printStackTrace();
    }
}

    private boolean isBookingConflict(String idLapangan, String tanggal, String jamMulai, String jamSelesai) {
    try {
        Connection c = Koneksi.getKoneksi();
        String query = "SELECT * FROM booking WHERE id_lapangan = ? AND tanggal = ? AND " +
                       "((? BETWEEN jam_mulai AND jam_selesai) OR " +
                       "(? BETWEEN jam_mulai AND jam_selesai) OR " +
                       "(jam_mulai BETWEEN ? AND ?) OR (jam_selesai BETWEEN ? AND ?))";

        PreparedStatement p = c.prepareStatement(query);
        p.setString(1, idLapangan);
        p.setString(2, tanggal);
        p.setString(3, jamMulai);
        p.setString(4, jamSelesai);
        p.setString(5, jamMulai);
        p.setString(6, jamSelesai);
        p.setString(7, jamMulai);
        p.setString(8, jamSelesai);

        ResultSet r = p.executeQuery();
        return r.next(); // true if conflict

    } catch (Exception e) {
        e.printStackTrace();
        return true; // anggap bentrok kalau error
    }
}
    
    public void loadData(){
        model.getDataVector().removeAllElements();
        
        model.fireTableDataChanged();
        
        try {
            Connection c = Koneksi.getKoneksi();
            Statement s = c.createStatement();
            
            String sql = "SELECT * FROM booking";
            ResultSet r = s.executeQuery(sql);
            
            while (r.next()){
                Object[] o = new Object[6];
                o [0] = r.getString("id_booking");
                o [1] = r.getString("id_lapangan");
                o [2] = r.getString("id_member");
                o [3] = r.getString("tanggal");
                o [4] = r.getString("jam_mulai");
                o [5] = r.getString("jam_selesai");
                
                model.addRow(o);
            }
            r.close();
            s.close();
        } catch (Exception e){
            System.out.println("terjadi kesalahan");
        }
    }
    
