    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        String idBooking = txtidbooking.getText().trim();
        String idLapangan = txtlapangan.getText().trim();
        String tanggal = txttanggal.getText().trim(); // Format: yyyy-MM-dd
        String jamMulai = txtjammulai.getText().trim(); // Format: HH:mm:ss
        String jamSelesai = txtjamselesai.getText().trim(); // Format: HH:mm:ss
        String id_member = uaspbo.LoginSession.getIdMember();


        // ✅ Validasi field wajib
        if (idLapangan.isEmpty() || tanggal.isEmpty() || jamMulai.isEmpty() || jamSelesai.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Semua kolom wajib diisi!");
            return;
        }

        // ✅ Validasi bentrok waktu
        if (isBookingConflict(idLapangan, tanggal, jamMulai, jamSelesai)) {
            JOptionPane.showMessageDialog(null, "⚠️ Waktu booking bentrok. Silakan pilih waktu lain.");
            return;
        }

        try {
            Connection c = Koneksi.getKoneksi();
            String sql = "INSERT INTO booking VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, idBooking);
            p.setString(2, idLapangan);
            p.setString(3, id_member);
            p.setString(4, tanggal);
            p.setString(5, jamMulai);
            p.setString(6, jamSelesai);
            p.executeUpdate();
            p.close();

            JOptionPane.showMessageDialog(null, "✅ Booking berhasil disimpan!");
            autonumberBooking();
            clearFields(); // pastikan ini mengosongkan semua txt field
            loadData();    // untuk refresh tabel

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "❌ Gagal menyimpan: " + e.getMessage());
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnsimpanActionPerformed

