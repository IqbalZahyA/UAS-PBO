    public void Cari() {
        DefaultTableModel tabel = new DefaultTableModel();

        tabel.addColumn("ID Lapangan");
        tabel.addColumn("Nama Lapangan");
        tabel.addColumn("Jenis Lapangan");

        try {
            Connection c = Koneksi.getKoneksi();
            String keyword = txtcari.getText().trim();

            // âœ… Gunakan spasi setelah tanda kutip SQL
            String sql = "SELECT * FROM lapangan WHERE id_lapangan LIKE '%" + keyword + "%' " +
                         "OR nama_lapangan LIKE '%" + keyword + "%'";

            Statement stat = c.createStatement();
            ResultSet rs = stat.executeQuery(sql);

            while (rs.next()) {
                tabel.addRow(new Object[]{
                    rs.getString("id_lapangan"),
                    rs.getString("nama_lapangan"),
                    rs.getString("jenis_lapangan")
                });
            }

            jTable1.setModel(tabel); // tampilkan hasil pencarian ke tabel

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Cari Data Error: " + e.getMessage());
        }
    }

    
